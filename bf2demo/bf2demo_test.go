package bf2demo

import (
	"bytes"
	"testing"

	"github.com/stretchr/testify/assert"
	"github.com/stretchr/testify/require"
)

func TestDecodeMetadata(t *testing.T) {
	testData := []byte{
		0x00, 0x00, 0x00, 0x00, // skip first 4 bytes
		0x0a, 0x00, 0x00, 0x00, // length of "ServerName"
		0x53, 0x65, 0x72, 0x76, 0x65, 0x72, 0x4e, 0x61, 0x6d, 0x65, // "ServerName"
		0x13, 0x00, 0x00, 0x00, // length of "2024-03-28 19:36:04"
		0x32, 0x30, 0x32, 0x34, 0x2d, 0x30, 0x33, 0x2d, 0x32, 0x38, 0x20, 0x31, 0x39, 0x3a, 0x33, 0x36, 0x3a, 0x30, 0x34, // "2024-03-28 19:36:04"
		0x07, 0x00, 0x00, 0x00, // length of "MapName"
		0x4d, 0x61, 0x70, 0x4e, 0x61, 0x6d, 0x65, // "MapName"
	}

	metadata, err := DecodeMetadata(bytes.NewReader(testData))
	require.NoError(t, err)

	assert.Equal(t, "ServerName", metadata.ServerName)
	assert.Equal(t, "2024-03-28 19:36:04", metadata.StartTime)
	assert.Equal(t, "MapName", metadata.MapName)
}
